/* Неразрывный пробел после коротких слов (предлоги, союзы, частицы),
   чтобы они не повисали в конце строки. \s в JS включает U+00A0, поэтому
   цепочки вроде «и в журнале» склеиваются за один проход. */
export function typo(text: string): string {
  return text.replace(/(?<=^|[\s«„(])([а-яёa-z]{1,3}) /gi, '$1 ')
}
